function stock(ticker: string, name: string, sector: string, tier: "Tier 1" | "Tier 2", price: number, quality: number, liquidity = 84, eventRisk = 5): StockSnapshot {
  return {
    ticker,
    name,
    sector,
    tier,
    price,
    movingAverage50: Number((price * 0.97).toFixed(2)),
    movingAverage200: Number((price * 0.91).toFixed(2)),
    rsi: 52,
    macdTrend: "Neutral",
    support: Number((price * 0.92).toFixed(2)),
    resistance: Number((price * 1.08).toFixed(2)),
    earningsDate: "2026-08-15",
    marketCapBillions: Number((price * 8).toFixed(1)),
    revenueGrowth: 0.08,
    freeCashFlowBillions: 8,
    debtToEquity: 0.55,
    roic: 0.14,
    grossMargin: 0.48,
    operatingMargin: 0.24,
    peg: 1.8,
    moatScore: Math.round(quality / 10),
    wheelQualityScore: quality,
    liquidityScore: liquidity,
    eventRiskScore: eventRisk
  };
}

